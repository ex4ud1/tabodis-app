/**
 * Translate `key` using `lang`, falling back to `es` then to the key itself.
 * Pure function — usable in Server Components, Client Components and tests.
 */
export function translate(lang: Lang, key: string): string {
  return DICT[lang]?.[key] ?? DICT.es[key] ?? key;
}

